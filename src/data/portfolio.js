export function toGeoJson(activities = geocvActivities) {
  return {
    type: "FeatureCollection",
    features: activities.map((activity) => ({
      type: "Feature",
      geometry: {
        type: "Point",
        coordinates: [activity.coordinates[1], activity.coordinates[0]]
      },
      properties: {
        id: activity.id,
        layer: activity.layer,
        organization: activity.organization,
        period: activity.period,
        place: activity.place,
        title: activity.title,
        title_en: activity.titleEn,
        summary: activity.short,
        summary_en: activity.shortEn,
        zoom: activity.zoom,
        logo: activity.logo?.initials ?? activity.organization,
        stack: activity.stack.join(", "),
        evidence: activity.evidence.map((link) => link.href).join(", "),
        repositories: activity.repositories.map((link) => link.href).join(", "),
        images: activity.images.map((item) => item.src ?? item.href).join(", "),
        awards: activity.awards.join(", ")
      }
    }))
  };
}
